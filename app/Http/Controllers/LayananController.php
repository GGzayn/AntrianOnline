    {
        $data = new Layanan;
        $data->opd_id = auth()->user()->child_id;
        $data->nama_layanan = $request->nama_layanan;

        $data->save();

        return redirect()->route('dinas.layanans.index')->with('status','new Layanan has Been Created');
    }

